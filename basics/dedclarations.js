/*

var
Declares a variable, optionally initializing it to a value.

let
Declares a block-scoped, local variable, optionally initializing it to a value.

const
Declares a block-scoped, read-only named constant.


1. With the keyword var. For example, var x = 42. to declare both local 
and global variables.

2. By simply assigning it a value. For example, x = 42. If this form is used 
outside of a function, it declares a global variable. 
It generates a strict JavaScript warning. 
You shouldn't use this variant.

3. With the keyword let. 
For example, let y = 13. This syntax can be used to declare a block-scope local variable.
**/